public class House {
    private String base;
    private String wall;
    private String roofed;

    public String getBase() {
        return base;
    }
    public void setBase(String baise) {
        this.base = baise;
    }
    public String getWall() {
        return wall;
    }
    public void setWall(String wall) {
        this.wall = wall;
    }
    public String getRoofed() {
        return roofed;
    }
    public void setRoofed(String roofed) {
        this.roofed = roofed;
    }
}
